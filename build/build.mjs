import { readFile, writeFile, mkdir } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const checkOnly = process.argv.includes("--check");

const readJson = async (path) => JSON.parse(await readFile(join(root, path), "utf8"));
const lang = (value, locale = "de") => value?.[locale] ?? value?.de ?? "";

const [scope, obligations, deadlines] = await Promise.all([
  readJson("data/scope.json"),
  readJson("data/obligations.json"),
  readJson("data/deadlines.json"),
]);

function assert(condition, message) {
  if (!condition) throw new Error(message);
}

function validate() {
  assert(scope.specialCases.length >= 5, "scope.json must contain special cases");
  assert(scope.sectors.some((sector) => sector.annex === "a1"), "scope.json must contain Annex 1 sectors");
  assert(scope.sectors.some((sector) => sector.annex === "a2"), "scope.json must contain Annex 2 sectors");
  assert(obligations.obligations.length >= 13, "obligations.json must contain the v1 obligation groups");
  assert(deadlines.deadlines.length >= 5, "deadlines.json must contain incident, registration, and change deadlines");

  const deadlineIds = new Set(deadlines.deadlines.map((deadline) => deadline.id));
  const deadlineGroupIds = new Set((deadlines.deadlineGroups ?? []).map((group) => group.id));
  for (const obligation of obligations.obligations) {
    assert(obligation.id && obligation.ref, `obligation missing id/ref: ${JSON.stringify(obligation)}`);
    for (const deadlineId of obligation.deadlineIds ?? []) {
      assert(deadlineIds.has(deadlineId), `unknown obligation deadline id: ${deadlineId}`);
    }
  }
  for (const row of scope.resultRows) {
    for (const deadlineId of row.deadlineIds ?? []) {
      assert(deadlineIds.has(deadlineId), `unknown scope result deadline id: ${deadlineId}`);
    }
    if (row.deadlineGroupId) {
      assert(deadlineGroupIds.has(row.deadlineGroupId), `unknown scope result deadline group id: ${row.deadlineGroupId}`);
    }
  }
}

const table = (headers, rows) => {
  const escape = (value) => String(value ?? "").replaceAll("|", "\\|").replaceAll("\n", " ");
  return [
    `| ${headers.map(escape).join(" | ")} |`,
    `| ${headers.map(() => "---").join(" | ")} |`,
    ...rows.map((row) => `| ${row.map(escape).join(" | ")} |`),
    "",
  ].join("\n");
};

function renderScope() {
  const specialRows = scope.specialCases.map((item) => [
    item.id,
    item.ref,
    lang(item.name),
    lang(item.description),
  ]);
  const sectorRows = scope.sectors.map((item) => [
    item.id,
    item.annex,
    lang(item.name),
    lang(item.description),
  ]);
  return [
    "# NIS2 Scope / Affectedness",
    "",
    "Generated from `data/scope.json`. Do not edit this file by hand.",
    "",
    "## Special Cases",
    "",
    table(["ID", "Ref", "Name", "Description"], specialRows),
    "## Sectors",
    "",
    table(["ID", "Annex", "Name", "Description"], sectorRows),
  ].join("\n");
}

function renderObligations() {
  const rows = obligations.obligations.map((item) => [
    item.id,
    item.ref,
    item.appliesTo.join(", "),
    lang(item.title),
    lang(item.subtitle),
    (item.deadlineIds ?? [])
      .map((id) => deadlines.deadlines.find((deadline) => deadline.id === id))
      .filter(Boolean)
      .map((deadline) => lang(deadline.label))
      .join(" · "),
  ]);
  return [
    "# NIS2 Obligations Matrix",
    "",
    "Generated from `data/obligations.json`. Do not edit this file by hand.",
    "",
    table(["ID", "Ref", "Applies to", "Title", "Subtitle", "Deadline"], rows),
  ].join("\n");
}

function renderDeadlines() {
  const rows = deadlines.deadlines.map((item) => [
    item.id,
    item.ref,
    lang(item.trigger),
    item.duration,
    lang(item.label),
  ]);
  return [
    "# NIS2 Deadlines",
    "",
    "Generated from `data/deadlines.json`. Do not edit this file by hand.",
    "",
    table(["ID", "Ref", "Trigger", "Duration", "Label"], rows),
  ].join("\n");
}

function renderJsonLd() {
  return {
    "@context": "https://schema.org",
    "@type": "Dataset",
    name: "Open NIS2 Dataset for Germany / BSIG",
    description: "Machine-readable NIS2 scope, obligations, and deadline reference data extracted from Directive Zero browser tools.",
    url: "https://github.com/directive-zero/nis2-data",
    version: "1.0.0",
    license: "https://joinup.ec.europa.eu/collection/eupl/eupl-text-eupl-12",
    creator: {
      "@type": "Organization",
      name: "Directive Zero",
      url: "https://directive0.eu"
    },
    distribution: [
      { "@type": "DataDownload", encodingFormat: "application/json", contentUrl: "https://github.com/directive-zero/nis2-data/blob/v1.0.0/data/scope.json" },
      { "@type": "DataDownload", encodingFormat: "application/json", contentUrl: "https://github.com/directive-zero/nis2-data/blob/v1.0.0/data/obligations.json" },
      { "@type": "DataDownload", encodingFormat: "application/json", contentUrl: "https://github.com/directive-zero/nis2-data/blob/v1.0.0/data/deadlines.json" }
    ]
  };
}

validate();

if (!checkOnly) {
  await mkdir(join(root, "dist"), { recursive: true });
  await Promise.all([
    writeFile(join(root, "dist/scope.md"), renderScope(), "utf8"),
    writeFile(join(root, "dist/obligations.md"), renderObligations(), "utf8"),
    writeFile(join(root, "dist/deadlines.md"), renderDeadlines(), "utf8"),
    writeFile(join(root, "dist/dataset.jsonld"), `${JSON.stringify(renderJsonLd(), null, 2)}\n`, "utf8"),
  ]);
}

console.log(checkOnly ? "nis2-data validation passed" : "nis2-data build completed");
